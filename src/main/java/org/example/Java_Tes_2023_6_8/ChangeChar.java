package org.example.Java_Tes_2023_6_8;

public class ChangeChar {
    public static void main(String[] args) {
        System.out.println("北\t上\t广");
        System.out.println("----------");
        System.out.println("北\n上\t广");
        System.out.println("----------");
        System.out.println("北\\上\\广");
        System.out.println("----------");
        System.out.println("北\'上\"广");
        System.out.println("----------");
        System.out.println("北\r上\r广");
        System.out.println("----------");


    }
}
